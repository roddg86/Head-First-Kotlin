fun main() {
    val x = 3;
    val y = 4

    /*
    * Использование if как выражения
    *
    * Использование if для возвращения значения
    *
    * Кошда if используется как выажение, нужно обязательно включать секцию else
    * */
    println(if (x > y) "x больше чем y" else "x не бльше чем y")
    println("Эта линия работает несмотря ни на что")
}